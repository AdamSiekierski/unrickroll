const puppeteer = require("puppeteer");
const readline = require("readline");
const chalk = require("chalk");
const { execSync } = require("child_process");

const EXTENSION_PATH = "./dist/src";

let browser;

const close = async () => {
  if (browser) {
    await browser.close();
  }
};

const build = async () => {
  try {
    console.log(chalk.yellow("Building the extension..."));
    execSync("yarn build");
    console.log(chalk.greenBright("Build finished"));
  } catch (err) {
    console.log(chalk.red("\nBuild failed:"));
    console.log(err);
    await close();
    process.exit();
  }
};

const develop = async () => {
  await close();
  await build();

  browser = await puppeteer.launch({
    defaultViewport: null,
    executablePath: process.env.chrome,
    headless: false,
    ignoreDefaultArgs: ["--disable-extensions"],
    args: [`--load-extension=${EXTENSION_PATH}`],
  });

  console.log(chalk.green("\nDevelopment browser started."));
  console.log(`Press ${chalk.cyan("r")} to restart.`);
  console.log(`Press ${chalk.red("Ctrl + C")} to close`);
};

readline.emitKeypressEvents(process.stdin);
process.stdin.setRawMode(true);
process.stdin.on("keypress", (_str, key) => {
  if (key.ctrl && key.name === "c") {
    close().then(() => {
      process.exit();
    });
  } else if (key.name === "r") {
    console.log(chalk.gray(`\n${"-".repeat(50)}`));
    console.log(chalk.yellow("\nRestarting development browser..."));
    develop();
  }
});

